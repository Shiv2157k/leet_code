from typing import List


class Sudoku:

    def is_valid(self, board: List[List[str]]) -> bool:
        """
        Approach: One Iteration
        Time Complexity: O(1)
        Space Complexity: O(1)
        :param board:
        :return:
        """
        board_size = len(board[0])
        rows = [{} for _ in range(board_size)]
        columns = [{} for _ in range(board_size)]
        boxes = [{} for _ in range(board_size)]

        for row in range(board_size):
            for col in range(board_size):
                num = board[row][col]
                box = (row // 3) * 3 + col // 3
                if num != ".":
                    rows[row][num] = rows[row].get(num, 0) + 1
                    columns[col][num] = columns[col].get(num, 0) + 1
                    boxes[box][num] = boxes[box].get(num, 0) + 1

                    if rows[row][num] > 1 or columns[col][num] > 1 or boxes[box][num] > 1:
                        return False
        return True


